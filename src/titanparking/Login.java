    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/parking?useSSL=false", "root", "teste@100");
            
            String username = edtUsername.getText();
            String password = edtPassword.getText();
            
            Statement stm = con.createStatement();
            String sql = "select * from tbl_usuario where usuario='"+username+"' and senha='"+password+"'";
            ResultSet rs = stm.executeQuery(sql);
            if(rs.next()) {
                dispose();
                Home hpage = new Home();
                hpage.show();
            }
            else {
                JOptionPane.showMessageDialog(this, "usuário ou senha invalidos");
                edtUsername.setText("");
                edtPassword.setText("");
            }
                con.close();
        }
        catch (Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

