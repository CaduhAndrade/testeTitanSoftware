        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/parking?useSSL=false", "root", "teste@100");
            String placa = txtPlaca.getText();
            String modelo = txtModelo.getText();
            String data = lblData.getText();
            String horario = lblHorario.getText();
            
            Statement stm = con.createStatement();
            String sql = "INSERT INTO tbl_movimentacao (placa, modelo, data_entrada, tempo) VALUES ('"+placa+"', '"+modelo+"', '"+data+"', '"+horario+"');";
            int result = stm.executeUpdate(sql);
            if(placa.isEmpty() && modelo.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "HÃ¡ campos vazios, verifique para continuar");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                Home hpage = new Home();
                hpage.show();
                dispose();
                con.close();
            }
        }
